class User {
  final String? id;
  final String? name;
  final String? last_name;
  final String? email;
  final String? password;
  final String? role;
  final String? depositID;
  final String? token;

  const User({
    this.id,
    this.name,
    this.last_name,
    this.email,
    this.password,
    this.role,
    this.depositID,
    this.token,
  });
}
